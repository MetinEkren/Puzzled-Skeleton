    public class LevelCamera : Camera
    {
        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor
        ////////////////////////////////////////////////////////////////////////////////////
        public LevelCamera(Player player)
            : base(0.0f, 0.0f)
        {
            Player = player;
        }

        ////////////////////////////////////////////////////////////////////////////////////
        // Methods
        ////////////////////////////////////////////////////////////////////////////////////
        virtual public void Update() { }

        public void Reset()
        {
            XOffset = 0.0f;
            YOffset = 0.0f;
        }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        public Player Player;

    }
