    //////////////////////////////////////////////////////////////////////////////////
    // VerticalCamera
    //////////////////////////////////////////////////////////////////////////////////
    public class VerticalCamera : Camera
    {
        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor
        ////////////////////////////////////////////////////////////////////////////////////
        public VerticalCamera(Player player)
            : base(0.0f, 0.0f)
        {
            m_Player = player;
        }

        ////////////////////////////////////////////////////////////////////////////////////
        // Methods
        ////////////////////////////////////////////////////////////////////////////////////
        public void Update()
        {
            if (m_Player.Position.Y < (7 * 48))
            {
                return;
            }
            else
            {
                YOffset = m_Player.Position.Y - (7 * 48);
            }
                
        }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        private Player m_Player;
    }
}
